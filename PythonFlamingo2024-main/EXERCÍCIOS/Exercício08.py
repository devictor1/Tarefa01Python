# Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a 
# área do retângulo. 

base = float(input('Insira abaixo o valor da base do retângulo'))
altura = float(input('Agora, insira o valor da altura do retângulo'))
area = base*altura
print("A área do seu retângulo é", area)