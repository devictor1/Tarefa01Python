# Escreva um algoritmo para ler as dimensões de um triângulo (base e altura), calcular e escrever a área. 

base = float(input('Insira abaixo o valor da base do triângulo'))
altura = float(input('Agora, insira o valor da altura do triângulo'))
area = (base*altura)/2
print("A área do seu triângulo é", area)